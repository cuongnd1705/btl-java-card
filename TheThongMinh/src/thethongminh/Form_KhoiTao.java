    private void button_fileSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_fileSelectActionPerformed
        JFileChooser fc = new JFileChooser();
        FileFilter imageFilter = new FileNameExtensionFilter("Image files", ImageIO.getReaderFileSuffixes());
        fc.setFileFilter(imageFilter);
        int showOpenDialog = fc.showOpenDialog(this);
        if (showOpenDialog == JFileChooser.APPROVE_OPTION) {
            File selectedImageFile = fc.getSelectedFile();
            String selectedImagePath = selectedImageFile.getAbsolutePath();

            try {
                customer.setAvatar(Files.readAllBytes(selectedImageFile.toPath()));
            } catch (IOException ex) {
                Logger.getLogger(Form_KhoiTao.class.getName()).log(Level.SEVERE, null, ex);
            }

            ImageIcon imageIcon = new ImageIcon(selectedImagePath);
            Image image = imageIcon.getImage().getScaledInstance(jLabelImage.getWidth(), jLabelImage.getHeight(), Image.SCALE_SMOOTH);
            jLabelImage.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_button_fileSelectActionPerformed

