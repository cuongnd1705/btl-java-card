    private void buttton_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttton_okActionPerformed
        // TODO add your handling code here:
        String pin = Arrays.toString(text_mapin.getPassword());
        String xnpin = Arrays.toString(text_xnmapin.getPassword());
        if ((pin.equals(xnpin)) != true) {
            JOptionPane.showMessageDialog(null, "Xác nhận mã pin sai");
        } else {
            if (pin.length() != 18) {
                JOptionPane.showMessageDialog(null, "Mã PIN phải có độ dài 6 kí tự");
            } else {
                String hoten = text_hoten.getText();
                String ngaysinh = text_ngaysinh.getText();
                String quequan = text_quequan.getText();
                String gioitinh = text_gioitinh.getText();

                customer.setPin(pin);
                customer.setName(hoten);
                customer.setGender(gioitinh);
                customer.setBirthDate(ngaysinh);
                customer.setAddress(quequan);
                JOptionPane.showMessageDialog(null, "Khởi tạo nội dung thẻ thành công. :)");
                text_gioitinh.setText("");
                text_hoten.setText("");
                text_mapin.setText("");
                text_ngaysinh.setText("");
                text_quequan.setText("");
                text_xnmapin.setText("");
            }
        }
    }//GEN-LAST:event_buttton_okActionPerformed

